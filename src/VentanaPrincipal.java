    /**
     * Metodo para agregar partidos desde la otra ventana
     * @param texto Partido para añadir
     */
    public void agregarPartido(String texto)
    {
        DefaultListModel a = new DefaultListModel();
        
        array.add(texto);
        for (int i = 0; i <array.size(); i++) {
            a.addElement(array.get(i));
        }
        jlLista1.setModel(a);
    }

