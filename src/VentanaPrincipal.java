/**
 * Método que rellena la quiniela, y lo muestra en la lista de resultados
 * @param evt Pulsar boton realizarquiniela
 */
    private void bRealizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRealizarActionPerformed
        Random aleatorio=new Random();
        for (int i = 0; i <array.size(); i++) {
            int numero=aleatorio.nextInt(3);
            String cadena=array.get(i);
            if(numero==0)
            {
                cadena=cadena+"-> X";
                array2.add(cadena);
            }
            else
            {
                cadena=cadena+"-> "+numero;
                array2.add(cadena);
            }
        }
        DefaultListModel a = new DefaultListModel();
        for (int i = 0; i <array2.size(); i++) {
            a.addElement(array2.get(i));
        }
        jlLista2.setModel(a);
        
    }//GEN-LAST:event_bRealizarActionPerformed

